public class WorkType {

    public int n;
    public String size;

    public WorkType(int n, String size) {
        this.n = n;
        this.size = size;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
